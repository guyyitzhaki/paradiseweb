                     <p>
                            In light of the policy of the city of Jerusalem to develop tourism, a group of artists decided to be part of this new trend. They wanted to generate a new resource for tourism as well as an independent income for local artists and the Mamuta Art and Media Center. The result: Paradise Inn based in the former Jerusalem Leper's Home at Hansen House.
                     </p>
                     <p>
                            Using the "green" approach to urban design as well as the idea of recycling electronic waste, the artists decided to create a summer hotel for temporary residence within the Hansen House gardens. Currently, these gardens are the home for the Jerusalem Center for Art, Design and Technology. The artists equally participated in the design and implementation of all aspects of the hotel, with the goal of giving everyone the possibility to spend a few nights in Paradise Inn. The project synthesizes new visionary ideas for a modern Jerusalem: a hotel for only one person, a personal paradise built in the exquisite Hansen House gardens.      </p>
                     <p>
                            Paradise Inn is an island surrounded by the walls of the former Jerusalem Leper's Home. A metaphor of differentiation and exclusivity, Paradise Inn is a luxurious paradise that originates from a public institution that was historically used to protect society from lepers and lepers from society. Paradise Inn dwells in the spirit of gentrification, inviting an artist to reside for free in the paradise for a month and a half. In return, the artist is expected to produce artwork for the comfort of future residents.
                     </p>

